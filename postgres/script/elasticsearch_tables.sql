CREATE TABLE IF NOT EXISTS tsunami (
    id INTEGER PRIMARY KEY,
    eventDate VARCHAR(100),
    eventValidity VARCHAR(50),
    causeCode VARCHAR(50),
    earthquakeEventId VARCHAR(50),
    numDeposits INTEGER,
    country VARCHAR(50),
    locationName VARCHAR(255),
    regionCode VARCHAR(50),
    latitude FLOAT,
    longitude FLOAT,
    numRunups INTEGER,
    tsIntensity FLOAT,
    deathsAmountOrder INTEGER,
    damageAmountOrder INTEGER,
    deathsAmountOrderTotal INTEGER,
    damageAmountOrderTotal INTEGER,
    publish BOOLEAN,
    oceanicTsunami BOOLEAN,
    volcanoEventId VARCHAR(50),
    maxWaterHeight FLOAT,
    eqMagnitude FLOAT,
    housesDestroyedAmountOrder INTEGER,
    deathsTotal INTEGER,
    housesDestroyedAmountOrderTotal INTEGER,
    tsMtIi FLOAT,
    deaths INTEGER,
    eqDepth FLOAT,
    housesDamagedAmountOrder INTEGER,
    housesDamagedAmountOrderTotal INTEGER,
    housesDestroyed INTEGER,
    housesDestroyedTotal INTEGER,
    area VARCHAR(50),
    injuries INTEGER,
    injuriesAmountOrder INTEGER,
    injuriesTotal INTEGER,
    injuriesAmountOrderTotal INTEGER,
    housesDamaged INTEGER,
    housesDamagedTotal INTEGER,
    missingTotal INTEGER,
    missingAmountOrderTotal INTEGER,
    damageMillionsDollarsTotal FLOAT,
    tsMtAbe FLOAT,
    damageMillionsDollars FLOAT,
    warningStatusId VARCHAR(50),
    missing INTEGER,
    missingAmountOrder INTEGER,
    regionName VARCHAR(255),
    cause VARCHAR(255),
    validity VARCHAR(255),
    warningStatus VARCHAR(255),
    year INTEGER,
    month INTEGER,
    day INTEGER,
    hour INTEGER,
    minute INTEGER,
    second FLOAT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);